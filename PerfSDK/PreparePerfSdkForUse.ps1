Param([string]$PerfSDKPath)

Add-Type -AssemblyName System.IO.Compression.FileSystem

function DownloadSeleniumDlls([string]$PerfSDKPath)
{
    $seleniumDllZipLocalPath = "C:\temp\selenium-dotnet-strongnamed-3.13.1.zip"
    $ieDriverZipLocalPath = "C:\temp\IEDriverServer_Win32_3.13.0.zip"
    $zipExtractionPath = "C:\temp\seleniumextraction"
    $seleniumSupportDllZipLocalPath = Join-Path $zipExtractionPath "dist\Selenium.Support.StrongNamed.3.13.1.zip"
    $seleniumWebDriverDllZipLocalPath = Join-Path $zipExtractionPath "dist\Selenium.WebDriver.StrongNamed.3.13.1.zip"
    
    #Selenium 3.13.1 and IEDriverServer 3.13.0 are required for single user UI testing after PU21
    Write-Host "Downloading Selenium version 3.13.1 and IEDriverServer version 3.13.0"   
    $WebClient = New-Object System.Net.WebClient
    $WebClient.DownloadFile("http://selenium-release.storage.googleapis.com/3.13/selenium-dotnet-strongnamed-3.13.1.zip", $seleniumDllZipLocalPath)
    $WebClient.DownloadFile("http://selenium-release.storage.googleapis.com/3.13/IEDriverServer_Win32_3.13.0.zip", $ieDriverZipLocalPath)

    Write-Host "Extracting Selenium zip file and IEServerDriver file"
    [System.IO.Compression.ZipFile]::ExtractToDirectory($seleniumDllZipLocalPath, $zipExtractionPath)
    [System.IO.Compression.ZipFile]::ExtractToDirectory($ieDriverZipLocalPath, $zipExtractionPath)

    #Only Dlls from .Support and .WebDriver from Selenium are required.
    Write-Host "Rename all nupkg files from Selenium 3.13.1 to zip extension"
    Dir (Join-Path $zipExtractionPath "dist\*.nupkg") | Rename-Item -NewName { [io.path]::ChangeExtension($_.name, "zip")}

    Write-Host "Extracting Selenium WebDriver and Support"
    [System.IO.Compression.ZipFile]::ExtractToDirectory($seleniumSupportDllZipLocalPath, (Join-Path $zipExtractionPath "Support"))
    [System.IO.Compression.ZipFile]::ExtractToDirectory($seleniumWebDriverDllZipLocalPath, (Join-Path $zipExtractionPath "WebDriver"))

    Write-Host "Making Selenium directory in PerfSDK directory"
    $seleniumPath = Join-Path $PerfSDKPath "Common\External\Selenium"
    New-Item -ItemType directory -Path $seleniumPath

    #Selenium 3.13.1 net45 version is required after PU21.
    Write-Host "Copying Selenium net45 dlls and IEDriverServer.exe to PerfSDK folder"
    Copy-Item (Join-Path $zipExtractionPath "IEDriverServer.exe") $seleniumPath
    Copy-Item (Join-Path $zipExtractionPath "Support\lib\net45\*") $seleniumPath
    Copy-Item (Join-Path $zipExtractionPath "WebDriver\lib\net45\*") $seleniumPath

    Write-Host "Cleaning up temp folder"
    Remove-Item -Path $seleniumDllZipLocalPath -Recurse
    Remove-Item -Path $ieDriverZipLocalPath -Recurse
    Remove-Item -Path $zipExtractionPath -Recurse
}

function DownloadOdbcDriverInstaller([string]$PerfSDKPath)
{
    Write-Host "Downloading ODBC driver $installer for online load tests"
    $odbcInstallerLocalPath = Join-Path $PerfSDKPath "Visual Studio Online\msodbcsql.msi"
    
    $WebClient = New-Object System.Net.WebClient
    $WebClient.DownloadFile("https://download.microsoft.com/download/E/6/B/E6BFDC7A-5BCD-4C51-9912-635646DA801E/en-US/msodbcsql_17.2.0.1_x64.msi", $odbcInstallerLocalPath)
}

function SetTestRootEnvironmentVariable([string]$PerfSDKPath)
{
    Write-Host "Setting the TestRoot environment variable to the PerfSDK path specified"
    [Environment]::SetEnvironmentVariable("TestRoot", $PerfSDKPath, "User")
}

